            <h2 class="text-center">Introdução</h2>
<hr class="primary">            <p>A psicanálise é um campo clínico e de investigação teórica da psique humana independente da Psicologia, que tem origem na Medicina, desenvolvido por Sigmund Freud, médico que se formou em 1881, trabalhou no Hospital Geral de Viena e teve contato com o neurologista francês Jean Martin Charcot, que lhe mostrou o uso da hipnose.</p>
        </div>
        <div class="col-8">
        <p>Freud, médico neurologista austríaco, propôs este método para a compreensão e análise do homem, compreendido enquanto sujeito do inconsciente, abrangendo três áreas:</p>
            <ol type="1">
                <li>um método de investigação do psiquismo e seu funcionamento;</li>
                <li>um sistema teórico sobre a vivência e o comportamento humano;</li>
                <li>um método de tratamento caracterizado pela aplicação da técnica da Associação Livre</li>
            </ol>
        <p>Essencialmente é uma teoria da personalidade e um procedimento de psicoterapia; a psicanálise influenciou muitas outras correntes de pensamento e disciplinas das ciências humanas, gerando uma base teórica para uma forma de compreensão da ética, da moralidade e da cultura humana.</p>
        <p>Em linguagem comum, o termo "psicanálise" é muitas vezes usado como sinônimo de "psicoterapia" ou mesmo de "psicologia". Em linguagem mais própria, no entanto, psicologia refere-se à ciência que estuda o comportamento e os processos mentais, psicoterapia ao uso clínico do conhecimento obtido por ela, ou seja, ao trabalho terapêutico baseado no corpo teórico da psicologia como um todo, e psicanálise refere-se à forma de psicoterapia baseada nas teorias oriundas do trabalho de Sigmund Freud; psicanálise é, assim, um termo mais específico, sendo uma entre muitas outras formas de psicoterapia.</p>
