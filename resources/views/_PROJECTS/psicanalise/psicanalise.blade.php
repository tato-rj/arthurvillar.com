            <h2 class="text-center">Definição</h2>
<hr class="primary">        </div>
        <div class="col-12">
        <p>De acordo com Sigmund Freud, psicanálise é o nome de um procedimento para a investigação de processos mentais que são quase inacessíveis por qualquer outro modo, (2) um método (baseado nessa investigação) para o tratamento de distúrbios neuróticos, e (3) uma coleção de informações psicológicas obtidas ao longo dessas linhas, e que gradualmente se acumulou numa "nova" disciplina científica. [5] A essa definição elaborada pelo próprio Freud pode ser acrescentada um tratamento possível da psicose e perversão, considerando o desenvolvimento dessa técnica.</p>
        <p>Ainda segundo o seu criador, a psicanálise cresceu num campo muitíssimo restrito. No início, tinha apenas um único objetivo — o de compreender algo da natureza daquilo que era conhecido como doenças nervosas ‘funcionais’, com vistas a superar a impotência que até então caracterizara seu tratamento médico. Em sua opinião, os neurologistas daquele período haviam sido instruídos a terem um elevado respeito por fatos químico-físicos e patológico-anatômicos e não sabiam o que fazer do fator psíquico e não podiam entendê-lo. Deixavam-no aos filósofos, aos místicos e — aos charlatães; e consideravam não científico ter qualquer coisa a ver com ele.</p>
        <p>Os primórdios da psicanálise datam de 1882 quando Freud, médico recém formado, trabalhou na clínica psiquiátrica de Theodor Meynert, e mais tarde, em 1885, com o médico francês Charcot, no Hospital Salpêtrière (Paris, França). Sigmund Freud, um médico interessado em achar um tratamento efetivo para pacientes com sintomas neuróticos ou histéricos. Ao escutar seus pacientes, Freud acreditava que seus problemas se originaram da inaceitação cultural, ou seja, seus desejos eram reprimidos, relegados ao inconsciente. Notou também que muitos desses desejos se tratavam de fantasias de natureza sexual. O método básico da psicanálise é o manejo da transferência e da resistência em análise. O analisado, numa postura relaxada, é solicitado a dizer tudo o que lhe vem à mente (método de associação livre). Suas aspirações, angústias, sonhos e fantasias são de especial interesse na escuta, como também todas as experiências vividas são trabalhadas em análise. Escutando o analisado, o analista tenta manter uma atitude empática de neutralidade. Uma postura de não-julgamento, visando a criar um ambiente seguro.</p>
        <p>A originalidade do conceito de inconsciente introduzido por Freud deve-se à proposição de uma realidade psíquica, característica dos processos inconscientes. Por outro lado, analisando-se o contexto da época observa-se que sua proposição estabeleceu um diálogo crítico à proposições Wilhelm Wundt (1832 — 1920) da psicologia com a ciência que tem como objeto a consciência entendida na perspectiva neurológica (da época) ou seja opondo-se aos estados de coma e alienação mental.</p>
        <p>Muitos colocam a questão de como observar o inconsciente. Se a Freud se deve o mérito do termo "inconsciente", pode-se perguntar como foi possível a ele, Freud, ter tido acesso a seu inconsciente para poder ter tido a oportunidade de verificar seu mecanismo, já que não é justamente o inconsciente que dá as coordenadas da ação do homem na sua vida diária.</p>
        <p>Não é possível abordar diretamente o inconsciente (Ics.), o conhecemos somente por suas formações: atos falhos, sonhos, chistes e sintomas diversos expressos no corpo. Nas suas conferências na Clark University (publicadas como Cinco lições de psicanálise) nos recomenda a interpretação como o meio mais simples e a base mais sólida de conhecer o inconsciente.</p>
        <p>Outro ponto a ser levado em conta sobre o inconsciente é que ele introduz na dimensão da consciência uma opacidade. Isto indica um modelo no qual a consciência aparece, não como instituidora de significatividade, mas sim como receptora de toda significação desde o inconsciente. Pode-se prever que a mente inconsciente é um outro "eu", e essa é a grande ideia de que temos no inconsciente uma outra personalidade atuante, em conjuntura com a nossa consciência, mas com liberdade de associação e ação.</p>
        <p>O modelo psicanalítico da mente considera que a atividade mental é baseada no papel central do inconsciente dinâmico. O contato com a realidade teórica da psicanálise põe em evidência uma multiplicidade de abordagens, com diferentes níveis de abstração, conceituações conflitantes e linguagens distintas. Mas isso deve ser entendido em um contexto histórico cultural e em relação às próprias características do modelo psicanalítico da mente.</p>
