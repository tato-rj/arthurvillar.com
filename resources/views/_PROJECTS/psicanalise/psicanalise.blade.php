            <h2 class="text-center">Correntes e Críticas</h2>
<hr class="primary">        </div>
        <div class="col-12">
        <p>Diversas dissidências da matriz freudiana foram sendo verificadas ao longo do século XX, tendo a psicanálise encontrado seu apogeu nos anos 50 e 60. As principais dissensões que passou o criador da psicanálise foram C. G. Jung e Alfred Adler, que participavam da expansão da psicanálise no começo do século XX. C. G. Jung, inclusive, foi o primeiro presidente do Instituto Internacional de Psicanálise (IPA), antes de sua renúncia ao cargo e a seguidor das ideias de Freud. Outras dissidências importantes foram Otto Rank, Erich Fromm e Wilhelm Reich. No entanto, a partir da teoria psicanalítica de Freud, fundou-se uma tradição de pesquisas envolvendo a psicoterapia, o inconsciente e o desenvolvimento da práxis clínica, com uma abordagem puramente psicológica.</p>
        <p>Desenvolvimentos como a psicoterapia humanista/existencial, psicoterapia reichiana, dentre diversas e tantas terapias existentes, foram, sem dúvida, influenciadas pela tradição psicanalítica, embora tenham conferido uma visão particular para os conteúdos da psicologia clínica.</p>
        <p>O método de interpretar os pacientes e buscar a cura de enfermidades físicas e mentais através de um diálogo sistemático/metodológico com os pacientes foi uma inovação trazida por Freud desenvolvido a partir de suas observações e experiência de tratamento através da hipnose. Até então, os avanços na área da psicoterapia eram obsoletas e tinham um apelo pela sugestão ou pela terapia com banhos, sangrias e outros métodos antigos no combate às doenças mentais.</p>
        <p>Sua contribuição para a Medicina, Psicologia, e outras áreas do conhecimento humano (arte, literatura, sociologia, antropologia, entre outras) é inegável[carece de fontes]. O verdadeiro choque moral provocado pelas ideias de Freud serviu para que a humanidade rompesse, ou pelo menos repensasse muito de seus tabus e preconceitos na compreensão da sexualidade, e atingisse um maior grau de refinamento e profundidade na busca das verdades psíquicas do ser humano.</p>
        <p>Na atualidade, a Psicanálise já não se limita à prática e tem uma amplitude maior de pesquisa, centrada em outros temas e cenários, desenvolvendo-se como uma ciência psicológica autônoma. Hoje fica muito difícil afirmar se a Psicanálise é uma disciplina da Psicologia ou uma Psicologia própria.</p>
        <p>Após Freud, muitos outros psicanalistas contribuíram para o desenvolvimento e importância da psicanálise. Entre alguns, podemos citar Melanie Klein, Winnicott, Bion e André Green. No entanto, a principal virada no seio da psicanálise, que conciliou ao mesmo tempo a inovação e a proposta de um "retorno a Freud" veio com o psicanalista francês Jacques Lacan. A partir daí outros importantes autores surgiram e convivem em nosso tempo, como Françoise Dolto, Serge André, J-D Nasio e Jacques-Alain Miller.</p>
        <p>Uma das recentes tendências é a criação da neuropsicanálise segundo Soussumi [11] tendo como antecedentes a fundação do grupo de estudos de neurociência e psicanálise no Instituto de Psicanálise em 1994 com a participação de Arnold Pfefer, e o neurocientista da Universidade de Columbia como James Schwartz, que a partir de 1996, fica sobre a coordenação de Mark Solms, psicanalista inglês com formação em neurociência, que vinha trabalhando em Londres e publicando trabalhos sobre o assunto desde a década de 1980 que juntamente com Pfeffer, em Londres, julho de 2000 , organizam o I Congresso Internacional de Neuro-Psicanálise, onde é criada a Sociedade Internacional de Neuro-Psicanálise.</p>
        <p>Destaca-se ainda nesse intere a publicação do artigo intitulado Biology and the future of psychoanalysis: a new intellectual framework for psychiatry (em português, “A biologia e o futuro da psicanálise: uma nova estrutura intelectual para a psiquiatria”) do neurocientista Eric Kandel, em 1999 . Segundo Kandel, a neurociência poderia fornecer fundamentos empíricos e conceituais mais sólidos à psicanálise. Um ano após a publicação do referido texto, em 2000, Kandel recebe o prêmio Nobel de medicina por suas contribuições à neurobiologia, introduzindo o conceito de plasticidade neural.</p>
