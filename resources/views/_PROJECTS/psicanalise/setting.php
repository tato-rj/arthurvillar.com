				<h3>O <i>Setting</i></h3>
				<img src="images/office.jpg" class="escritorio">
				<p>O que é o setting psicanalítico?</p>
				<p>O setting, segundo Zimerman (1999), é "a soma de todos os procedimentos que
				organizam, normatizam e possibilitam o processo psicanalítico". Em outras palavras, o
				setting psicanalítico (ou “ajuste psicanalítico”) é um espaço onde são criadas todas
				condições básicas para que o tratamento do paciente se desenvolva.</p>
				<p>Esse espaço, a que chamamos setting, abrange não só o espaço físico de atuação do
				psicanalista, mas também o conjunto das normas e princípios que orientam a relação
				entre o analisando e o analista, tais como o respeito à ética e a confiança no processo
				terapêutico.</p>
				<p>Considera-se que o grande centro ético da psicanálise é o reconhecimento da realidade
				psíquica e o amor à verdade. Tais elementos constituem o cerne da função do analista na
				investigação do funcionamento mental do analisando. (FREUD, 1940/1978)
				A formação setting é estabelecida no início do tratamento psicanalítico e será criado
				conforme as necessidades dos dois integrantes do processo terapêutico: o analista e o
				paciente em tratamento.  Caberá ao psicanalista ter a sensibilidade de perceber as
				necessidades do paciente ao longo do tratamento terapêutico para que este possa se
				desenvolver de maneira satisfatória.</p>
			</li>
			<li>
				<h3>A duração da terapia</h3>
				<p>Tendo em vista as características e as necessidades de cada paciente, a terapia
				pasicanalítica pode ser de curto prazo (durando alguns meses), de longo prazo (durando
				anos), ou mesmo se dar ao longo de um pequeno número de sessões. Vejamos:
				A terapia psicanalítica de curto prazo é normalmente desenvolvida ao longo de 16 a 14
				sessões, normalmente uma vez por semana. Neste tipo de terapia, o paciente e o
				terapeuta devem estabelecer quais as questões particulares em que a tratamento irá se concentrar.</p>
				<p>Na terapia pasicanalítica de longo prazo já é possível tratar o paciente de maneira mais
				ampla, bem como explorar os temas que vão surgindo ao longo do caminho.</p>
				<h3>Duração da sessão</h3>
				<p>As sessões psicanalíticas duram sempre 50 minutos. Porém, na consulta inicial da
				terapia on-line, o analista pode exceder esse tempo em 10 minutos ou mais. O paciente
				deve estar atento para chegar na hora certa, pois o atrazo significa perda de parte da
				sessão.</p>
				<p>As sessões terapêuticas com periodicidade de uma vez por semana podem ser o
				suficiente, mas em alguns casos específicos o paciente pode apresentar características
				que recomendem uma frequência maior de sessões.</p>

			</li>
			<li>
				<a name="terapia_online"><h3>A terapia online</h3></a>
				<p>A psicoterapia é conduzida com uma web câmara com a utilização do skype. Tenho praticado esse modelo de terapia com brasileiros residentes em diversos Estados do Brasil. O acesso online permite um conforto adicional pelo fato do paciente poder estar em seu ambiente e se poupar dos transtornos inevitáveis que o deslocamento impõe. Há uma economia notável de dinheiro e tempo.</p>
				<img src="images/image004.jpg" class="trabalho">
				<p>A psicanálise online pode ajudar muito pessoas com distúrbios de ansiedade, depressão, e dificuldades emocionais  consigo mesmas e com os outros. É muito importante que o paciente disponha de um espaço reservado para o atendimento, salvo de ruídos e movimento que poderiam prejudicar o bom aproveitamento do trabalho. A psicanálise online pode ser realizada em outros ambientes, se o paciente estiver no escritório ou viajando, desde que as características de privacidade sejam respeitadas.</p>
