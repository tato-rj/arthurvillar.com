<hr class="primary">        </div>
    </div>
    <div class="row">
        <div class="col-12">
            <p>Tendo em vista as características e as necessidades de cada paciente, a terapia
            pasicanalítica pode ser de curto prazo (durando alguns meses), de longo prazo (durando
            anos), ou mesmo se dar ao longo de um pequeno número de sessões. Vejamos:
            A terapia psicanalítica de curto prazo é normalmente desenvolvida ao longo de 16 a 14
            sessões, normalmente uma vez por semana. Neste tipo de terapia, o paciente e o
            terapeuta devem estabelecer quais as questões particulares em que a tratamento irá se concentrar.</p>
            <p>Na terapia pasicanalítica de longo prazo já é possível tratar o paciente de maneira mais
            ampla, bem como explorar os temas que vão surgindo ao longo do caminho.</p>
        </div>
