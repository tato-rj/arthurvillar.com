                <h2>Biography</h2>
                
                <p>Chia-Ying Chan, originally from Taiwan, brings passion, color, sensitivity and creativity to her interpretations of classical music. She has given solo performances and recitals across Taiwan, America and Europe, including at Carnegie Hall’s Weill Hall in New York and international festivals such as the Shuan-yin International Music Festival in Taiwan, the Piano Summer at New Paltz in America and the Gijón International Piano Festival in Spain.</p>

                <p>Chan has won top prizes in competitions such as the 2016 American Fine Arts Festival International Concerto Competition (First Place), the 2014 West Virginia University Keyboard competition (First Prize), 2014 American Protégé International Competition of Romantic Music (First Place), and the National University of Tainan (NUTN) Concerto Competition in Taiwan (Winner). She made her orchestral debut with the National University of Tainan Symphony Orchestra in Beethoven’s Piano Concerto no. 1 in 2006, and received an Exceptional Merit Award from NUTN in 2007.</p>

                <p>Press from her solo debut in Taiwan reported “… her passionate interpretation and emotional range led audiences into the composers’ fantasy world…..”. She has been selected to attend masterclasses and festivals with world- renowned masters such as Robert McDonald, Alberto Nose, Christopher Taylor, John Perry, Peter Takács, Julian Martin, Jose Ramon Mendez, Vladimir Feltsman, Vladimir Ovchinnikov, and others. Chan has been awarded scholarships for study at universities and festivals throughout the US, Taiwan, and Europe. In addition to performing solo piano works, Chan has been an active collaborator in both chamber and accompanist settings. Her repertoire encompasses a wide range of works including composers from the baroque to the present.</p>

                <p>Chan is currently a Doctor of Musical Arts student of Timothy Ehlen at the University of Illinois at Urbana-Champaign. Her DMA research project focused on the piano works composed by American composer Harold Shapero”. Her principal teachers have included Paul Schenly and Kathryn Brown at the Cleveland Institute of Music, where she received her Master’s degree. She earned her Bachelor of Music in Taiwan, where she studied with Ching-Jen Wu.</p>
